const puppeteer = require("puppeteer");
const fs = require("fs");

const sleep = (ms) => new Promise((res) => setTimeout(res, ms));

async function scrapeCategory(browser, url, columns, maxPages = 10) {
  const page = await browser.newPage();
  await page.setUserAgent("Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36");

  let allItems = [];

  for (let pageNum = 1; pageNum <= maxPages; pageNum++) {
    console.log(`  Fetching page ${pageNum}/${maxPages}`);
    await page.goto(`${url}/#page=${pageNum}`, { waitUntil: "domcontentloaded", timeout: 60000 });
    await sleep(3000);

    const items = await page.evaluate((cols) => {
      const rows = document.querySelectorAll("#category_content tr.tr__product");
      return Array.from(rows)
        .map((row) => {
          const tds = row.querySelectorAll("td");
          const obj = {};
          for (const [key, idx] of Object.entries(cols)) {
            let val = tds[idx]?.innerText?.trim() || null;
            if (val) {
              const parts = val.split("\n");
              val = parts[parts.length - 1].trim() || null;
            }
            obj[key] = val;
          }
          const nameRaw = tds[1]?.innerText?.trim() || null;
          obj.name = nameRaw ? nameRaw.split("\n")[0].trim() : null;
          const priceRaw = tds[tds.length - 1]?.innerText?.replace("Add", "").replace("$", "").replace(",", "").trim();
          obj.price = parseFloat(priceRaw) || null;
          return obj;
        })
        .filter((r) => r.name);
    }, columns);

    if (!items || items.length === 0) {
      console.log(`  No items on page ${pageNum}, stopping`);
      break;
    }

    allItems = allItems.concat(items);
    console.log(`  Got ${items.length} (total: ${allItems.length})`);
    await sleep(1500);
  }

  await page.close();
  return allItems;
}

